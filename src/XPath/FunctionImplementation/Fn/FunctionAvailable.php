namespace Jdomenechb\XSLT2Processor\XPath\FunctionImplementation\Fn;

use Jdomenechb\XSLT2Processor\XPath\FunctionImplementation\AbstractFunctionImplementation;
use Jdomenechb\XSLT2Processor\XPath\XPathFunction;

/**
 * Function function-available() from XSLT standard library.
 */
class FunctionAvailable extends AbstractFunctionImplementation
{
    /**
     * {@inheritdoc}
     *
     * @param XPathFunction $func
     * @param $context
     *
     * @return string
     */
    public function evaluate(XPathFunction $func, $context)
    {
        $functionName = $func->getParameters()[0]->evaluate($context);

        $function = clone $func;
        $function->setFullName($functionName);

        // No need to reset parameters, as we don't use them for checking existance

        return $function->exists();
    }
}
