namespace Jdomenechb\XSLT2Processor\XPath\FunctionImplementation\Xs;

use Jdomenechb\XSLT2Processor\XPath\Expression\Converter;
use Jdomenechb\XSLT2Processor\XPath\FunctionImplementation\AbstractFunctionImplementation;
use Jdomenechb\XSLT2Processor\XPath\XPathFunction;

/**
 * Function xs:integer() from XSLT standard library.
 */
class FuncInteger extends AbstractFunctionImplementation
{
    /**
     * {@inheritdoc}
     *
     * @param XPathFunction $func
     * @param $context
     *
     * @return string
     */
    public function evaluate(XPathFunction $func, $context)
    {
        return Converter::fromDOMToInteger($func->getParameters()[0]->evaluate($context));
    }
}
